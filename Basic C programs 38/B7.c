//WAP to store the five vowels(a,e,o,i,u) in five suitably declared variables.then print out the contents of the variables on a single line//
#include<stdio.h>
int main()
{
	char a,e,i,o,u;
	a='a';
	e='e';
	i='i';
	o='o';
	u='u';
	printf("%c",a);
	printf("%c",e);
	printf("%c",i);
	printf("%c",o);
	printf("%c",u);
	return 0;
}
